package com.zhang.base;

public class Demo1 {
    public static void main(String[] args){
        //整数
        String a="hello";
        int num = 10;//最常用
        byte num2 = 20;
        short num3 = 30;
        long num4 = 30L;//Long类型要在数字后面价格L
        //小数
        float num5 = 50.1F;//小数要在后面加F
        double num6 = 3.1415928;
        //字符
        char name = 'A';
        //布尔值
        boolean flag = true;

        System.out.println(a);
        System.out.println(flag);
    }
}
